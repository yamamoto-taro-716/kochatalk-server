<?php
namespace App\Model\Table;

use Cake\ORM\Query;
use Cake\ORM\RulesChecker;
use Cake\ORM\Table;
use Cake\Validation\Validator;

/**
 * Settings Model
 *
 * @method \App\Model\Entity\Setting get($primaryKey, $options = [])
 * @method \App\Model\Entity\Setting newEntity($data = null, array $options = [])
 * @method \App\Model\Entity\Setting[] newEntities(array $data, array $options = [])
 * @method \App\Model\Entity\Setting|bool save(\Cake\Datasource\EntityInterface $entity, $options = [])
 * @method \App\Model\Entity\Setting patchEntity(\Cake\Datasource\EntityInterface $entity, array $data, array $options = [])
 * @method \App\Model\Entity\Setting[] patchEntities($entities, array $data, array $options = [])
 * @method \App\Model\Entity\Setting findOrCreate($search, callable $callback = null, $options = [])
 */
class SettingsTable extends Table
{

    /**
     * Initialize method
     *
     * @param array $config The configuration for the Table.
     * @return void
     */
    public function initialize(array $config)
    {
        parent::initialize($config);

        $this->setTable('settings');
        $this->setDisplayField('id');
        $this->setPrimaryKey('id');
    }

    /**
     * Default validation rules.
     *
     * @param \Cake\Validation\Validator $validator Validator instance.
     * @return \Cake\Validation\Validator
     */
    public function validationDefault(Validator $validator)
    {
        $validator
            ->integer('id')
            ->allowEmpty('id', 'create');

        $validator
            ->integer('day_message')
            ->allowEmpty('day_message');

        $validator
            ->integer('count_ads')
            ->allowEmpty('count_ads');

        $validator
            ->scalar('title_ads')
            ->allowEmpty('title_ads');

        $validator
            ->scalar('content_ads')
            ->allowEmpty('content_ads');

        $validator
            ->scalar('term_ja')
            ->allowEmpty('term_ja');

        $validator
            ->scalar('term_en')
            ->allowEmpty('term_en');

        $validator
            ->scalar('policy_ja')
            ->allowEmpty('policy_ja');

        $validator
            ->scalar('policy_en')
            ->allowEmpty('policy_en');

        return $validator;
    }
}
